CREATE SEQUENCE <sequence_name>
INCREMENT BY <integer>
START WITH <integer>
MAXVALUE <integer> / NOMAXVALUE
MINVALUE <integer> / NOMINVALUE
CYCLE / NOCYCLE
CACHE <#> / NOCACHE
ORDER / NOORDER; 

SQL>CREATE SEQUENCE MY_SEQUENCE    
	INCREMENT BY 1  START WITH 0 MINVALUE 0  MAXVALUE 99999999999    
    CACHE 20 CYCLE  ORDER ;
	
SQL>ALTER SEQUENCE MY_SEQUENCE    
	INCREMENT BY 1  MAXVALUE 99999999999  CACHE 20
	CYCLE ORDER ;	
	
SQL>DROP SEQUENCE MY_SEQUENCE;
SQL>SELECT MY_SEQUENCE.NEXTVAL,MY_SEQUENCE.CURRVAL FROM dual;
SQL>SELECT * FROM USER_SEQUENCES WHERE SEQUENCE_NAME='MY_SEQUENCE'