SELECT NVL(T1.ID,0) AS TABLEONE_ID ,NVL(T1.NAME,'NO DATA FOUND')AS TABLONE_NAME
      ,NVL(T2.ID,0)AS TABLETWO_ID,NVL(T2.NAME,'NO DATA FOUND')AS TABLETWO_NAME
FROM TABLEONE T1,TABLETWO T2
WHERE T1.NAME(+) =T2.NAME
